namespace Rick
{
    /// <summary>
    /// The gender of a character, used for searching
    /// </summary>
    public enum Gender
    {
        /// <summary>
        /// The character is a female
        /// </summary>
        female,
        /// <summary>
        /// The character is a male
        /// </summary>
        male,
        /// <summary>
        /// The character is neither a male or female
        /// </summary>
        genderless,
        /// <summary>
        /// It is not known what gender this character is.
        /// </summary>
        uknown
    }

}
